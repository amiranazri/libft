#include "libft.h"

void	ft_putchar_fd(char c, int fd);

void	ft_putstr_fd(char const *s)
{
	int i;
	
	i = 0;
	while (s[i])
	{
		ft_putchar_fd(s[i]);
		i++;
	}
}
