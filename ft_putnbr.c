#include <unistd.h>
#include <stdio.h>

int		ft_putnbr(int nbr)
{

	write(1, &nbr, 1);
	return (0);
}

int		main()
{
	int n;

	n = 0;
	printf ("%d", ft_putnbr(n));
	return (0);
}
