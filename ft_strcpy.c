#include <string.h>
#include <stdio.h>

char	*ft_strcpy(char *dest, const char *src)
{
	char *start;
	
	start = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (start);
}

int		main()
{
	printf("%s", ft_strcpy("Hello ", "bitch."));
}
