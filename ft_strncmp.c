#include <string.h>

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned long int i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0' && i < n - 1)
		i++;
	if(s1[i] != s2[i])
	{
		n = s1[i] - s2[i];
		return (n);
	}
	else
		return (0);
}
